package com.rai.shubham;

public class SwitchEx {

    public static void main(String[] args)

    {
        int num=30;
        switch (num){ //expression
            case 0:
                System.out.println("zero");
                break;
            case 1:
                System.out.println("One");
                break;
            case 2:
                System.out.println("Two");
                break;
            default:
                System.out.println("invalid value");
        }
    }
}
