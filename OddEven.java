public class OddEven {

    public static void main(String[] args)
    {

        System.out.println("Here we are printing even and odd no");

        int n=3;
        if (n%2==0)
        {
            System.out.println("n is even");
        }
        else
        {
            System.out.println("n is odd");
        }
    }
}
